#Python-ში slicing-ის დახმარებით შექმენით პროგრამა, რომელიც ამოჭრის მომხმარებლის მიერ შემოტანილ სტრინგს იმ ინდექსიდან, რომელსაც ასევე მომხმარებელი შემოიტანს


# მომხმარებლისგან ვიღებთ ტექსტს
text = input("შეიყვანეთ ტექსტი: ")

# მომხმარებლისგან ვიღებთ დაწყებისა და დამთავრების ინდექსებს
start = int(input("შეიყვანეთ დასაწყისი ინდექსი: "))
end = int(input("შეიყვანეთ საბოლოო ინდექსი: "))

# ვაკეთებთ slicing-ს
sliced_text = text[start:end]

print("ამოჭრილი ტექსტი:", sliced_text)




