#შექმენით პროგრამა, რომელიც მიიღებს input-ად წინადადებას და დააბრუნებს მასში არსებულ სიტყვებს შემოტრიალებულად, მაგრამ იგივე თანმიმდევრობით.







# ვიღებთ წინადადებას
sentence = input("შეიყვანეთ წინადადება: ")

# ვშლით წინადადებას სიტყვებად და ვაბრუნებთ თითოეულ სიტყვას
reversed_words = " ".join(word[::-1] for word in sentence.split())

print("დაბრუნებული სიტყვები:", reversed_words)
