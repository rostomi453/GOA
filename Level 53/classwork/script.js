// ფუნქცია default პარამეტრებით
function greet(name = "მეგობარო", age = 18) {
    console.log(`გამარჯობა, ${name}! შენი ასაკია ${age}.`);
}

// ფუნქციის გამოყენება:
greet(); // გამარჯობა, მეგობარო! შენი ასაკია 18.
greet("rostom", 25); // გამარჯობა, ნიკა! შენი ასაკია 25.
greet("ანი"); // გამარჯობა, ანი! შენი ასაკია 18.
